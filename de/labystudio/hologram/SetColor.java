package de.labystudio.hologram;

public class SetColor
{
  float r;
  float g;
  float b;
  float a;
  
  public SetColor(float r, float g, float b, float a)
  {
    this.r = r;
    this.g = g;
    this.b = b;
    this.a = a;
  }
  
  public float getR()
  {
    return r;
  }
  
  public float getB()
  {
    return b;
  }
  
  public float getA()
  {
    return a;
  }
  
  public float getG()
  {
    return g;
  }
}
